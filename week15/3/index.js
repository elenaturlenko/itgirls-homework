function show() {

  let = black = '#483636';

    switch(document.getElementById('color').value) {
        case 'dark':
            document.body.style.background = black;
            break;

        case 'white':
            document.body.style.background = '#EFC5C5';
            break;
        
        case 'blue':
            document.body.style.background = '#39A9DE';
            break;

    }
  
}